    private void coursesNotFullActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_coursesNotFullActionPerformed
        studentTextArea.setText(null);
        courseID.setVisible(false);
        for(int i = 0; i < Admin.course_data.size(); i++) {
            if(!(Admin.course_data.get(i).getCurrentStudents().equals((Admin.course_data).get(i).getMaxStudents()))) {
                    studentTextArea.append("This course is not full: " + Admin.course_data.get(i).getCourseName() + "\n");

            }
            else {
                    studentTextArea.append("All courses are full.");
            }
        }
    }//GEN-LAST:event_coursesNotFullActionPerformed

