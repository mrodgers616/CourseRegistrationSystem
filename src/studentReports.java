    private void studentActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentActionActionPerformed
        if (studentAction.getText().equals("Register")) {
            String id = courseID.getText();
            
            for(int i = 0; i < Admin.course_data.size(); i++) {
                if(!Admin.course_data.get(i).getMaxStudents().equals(Admin.course_data.get(i).getCurrentStudents())) {
                    if(Admin.course_data.get(i).getCourseID().equals(id)) {
                        for(int j = 0; j < Courses.Students.size(); j++) {
                                if((!Admin.course_data.get(i).getStudentNames().get(j).equals(Courses.Students.get(j).getName())) || (Admin.course_data.get(i).getStudentNames().isEmpty())) {
                                    if(Home.currentName.equals(Courses.Students.get(j).getName())) {
                                        Admin.course_data.get(i).addStudentNames(Courses.Students.get(j).getName());
                                        int new_students = Integer.parseInt(Admin.course_data.get(i).getCurrentStudents());
                                        new_students++;
                                        String student_amount = String.valueOf(new_students);
                                        Admin.course_data.get(i).setCurrentStudents(student_amount);

                                        studentTextArea.append("Success!");
                                        courseID.setVisible(false);
                                        studentAction.setVisible(false);
                                    }
                                }
                        }
                       
                    }
                }
                else {
                        studentTextArea.append("Course if full");

                }
            }
        }
        else if (studentAction.getText().equals("Withdraw")) {
            String id = courseID.getText();
            Student s = new Student();
            if(s.withdrawFromCourse(id)) {
                studentTextArea.setText(null);
                studentTextArea.append("Withdrawn from course");
                courseID.setVisible(false);
                studentAction.setVisible(false);
            }
            else {
                studentTextArea.setText(null);
                //studentTextArea.append("Unable to withdraw from course\n");
                
            }
           
        }
    }//GEN-LAST:event_studentActionActionPerformed

