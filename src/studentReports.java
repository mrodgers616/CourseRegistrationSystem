    private void studentCoursesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentCoursesActionPerformed
        courseID.setVisible(false);
        studentAction.setVisible(false);
        
        studentTextArea.setText(null);
        for(int i = 0; i < Admin.course_data.size(); i++) {
            for(int j =0; j <Courses.Students.size(); j++) {
              if(Admin.course_data.get(i).getStudentNames().contains(Courses.Students.get(j).getName())) {
                if(i == 0) {
                    studentTextArea.append("The student is registered in the following courses: \n");
                }

                studentTextArea.append(Admin.course_data.get(i).getCourseName() + "\n");
                }  
            }
            

        }

    }//GEN-LAST:event_studentCoursesActionPerformed

