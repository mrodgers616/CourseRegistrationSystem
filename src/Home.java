    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        Admin admin = new Admin("Admin");
        Student student = new Student();
        ArrayList<Student> studentList = student.getStudents();
        adminHome adminLanding = new adminHome();
        studentReports studentLanding = new studentReports();
        String username = usernameLogin.getText();
        String password = passwordLogin.getText();
        if(admin.getUsername().equals(username) && admin.getPassword().equals(password)) {
            adminLanding.setVisible(true);
            dispose();
            
        }
        else if(!studentList.isEmpty()) {
            for(int i =0; i < studentList.size(); i++) {
                if(studentList.get(i).getUsername().equals(username) && studentList.get(i).getPassword().equals(password)) {
                studentLanding.setVisible(true);
                dispose();
                currentName = studentList.get(i).getName();
                } 
                else {
                    invalid.setVisible(true);
                }
            }
            
        }
        
        else {
            invalid.setVisible(true);
        }
    }//GEN-LAST:event_loginActionPerformed

    
